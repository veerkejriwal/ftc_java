public class Assignment_Operators {
    public static void main(String[] args) {
        int a = 5, b = 20, c = 1;
        b -= a;
        a += 10;    
        c *= b;    
 
        System.out.println("a = " + a);  
        System.out.println("b = " + b);  
        System.out.println("c = " + c);  
    }
}